package corejava;

public class Loops {

	public static void main(String[] args) {

		//for loop
		for(int i=0;i<5;i++)
		{
			System.out.println("Value of i is "+i);
		}
		System.out.println("\n");

		//while loop
		int k=0;
		while(k<5)
		{
			System.out.println("Value of k is "+k);
			k++;
		}

		System.out.println("\n");
		//do-while loop
		int j=0;
		do
		{
			System.out.println("Value of j is "+j);
			j++;
		}while(j<5);

	}

}
